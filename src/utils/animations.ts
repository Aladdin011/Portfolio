/**
 * Get animation variants based on viewport
 */
export const getViewportAnimation = (offset = 0) => ({
  initial: { opacity: 0, y: 30 },
  whileInView: { opacity: 1, y: 0 },
  transition: { duration: 0.8, ease: "easeOut" },
  viewport: { once: true, amount: 0.2, margin: `${offset}px` },
});

/**
 * Get staggered children animation
 */
export const getStaggeredChild = (index: number, delay: number = 0.1) => ({
  initial: { opacity: 0, y: 20 },
  animate: { opacity: 1, y: 0 },
  transition: {
    duration: 0.5,
    delay: index * delay,
  },
});
